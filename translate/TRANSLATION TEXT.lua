local function SetLang()
	local Text_Group_Variable = "AllTranslate"
    local lang_set = {"tha","tw","en","ita","tur","ger","cn","jpn","ptb","esn","ind","vie","msa","rus","fra","kor","ara"}
    Valuegroup:clearGroupByName(18, Text_Group_Variable, 0)
    for i,v in ipairs(lang_set) do
        for a,datas in pairs(StringConfig[v]) do
            local txt = tostring("["..v.."]".."["..a.."]"..datas)
            Valuegroup:insertInGroupByName(18, Text_Group_Variable, txt, 0)
        end
        threadpool:wait(0.01)
    end
end
